package com.example.calcv3;

public class Symbols {
    private char add = '+';
    private char minus = '-';
    private char multiply = '\u00D7';
    private char divide = '\u00F7';
    private char equal = '=';
    private char dot = '.';

    public char getAdd() {
        return add;
    }

    public char getMinus() {
        return minus;
    }

    public char getMultiply() {
        return multiply;
    }

    public char getDivide() {
        return divide;
    }

    public char getEqual() {
        return equal;
    }

    public char getDot() {
        return dot;
    }
}
